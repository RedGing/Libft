#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*rt;

	rt = NULL;
	while (*s != '\0' && *s != (char)c)
		++s;
	if (*s == (char)c)
		rt = (char *)s;
	return (rt);
}
